	//	R	G	B
	glColor3f(1.0f, 0.0f, 0.0f);

	// Narysowanie prostok¹ta wype³nionego aktualnym kolorem
	/*glRectf(x1, y1, x1 + rsize, y1 + rsize);*/
	glBegin(GL_POLYGON);
	glVertex3f(pozX + x1, pozY + y1, 0.0f);
